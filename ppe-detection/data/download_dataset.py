#!/usr/bin/env python3
"""
download_dataset.py — Download the Construction Site Safety dataset from Roboflow.

The script authenticates with the Roboflow API, downloads the specified dataset
version in YOLOv8 format, and patches configs/dataset.yaml so that all downstream
scripts (train, evaluate, compare, visualize) point to the correct data location.

Usage:
    python data/download_dataset.py \\
        --api-key  <YOUR_ROBOFLOW_API_KEY>  \\
        --workspace roboflow-universe-projects \\
        --project   construction-site-safety  \\
        --version   1

Find your API key at: https://app.roboflow.com/ → Settings → Roboflow API
"""

import argparse
import os
import shutil
import sys
from pathlib import Path

# ── Resolve project root so relative imports work regardless of cwd ──────────
PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATASET_YAML  = PROJECT_ROOT / "configs" / "dataset.yaml"
DEFAULT_DST   = PROJECT_ROOT / "data" / "construction_site_safety"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Download Construction Site Safety dataset from Roboflow"
    )
    parser.add_argument(
        "--api-key", required=True,
        help="Roboflow API key (from app.roboflow.com → Settings → API)"
    )
    parser.add_argument(
        "--workspace", default="roboflow-universe-projects",
        help="Roboflow workspace slug (default: roboflow-universe-projects)"
    )
    parser.add_argument(
        "--project", default="construction-site-safety",
        help="Roboflow project slug (default: construction-site-safety)"
    )
    parser.add_argument(
        "--version", type=int, default=1,
        help="Dataset version number (default: 1)"
    )
    parser.add_argument(
        "--location", default=str(DEFAULT_DST),
        help=f"Where to save the dataset (default: {DEFAULT_DST})"
    )
    return parser.parse_args()


def download(args: argparse.Namespace) -> Path:
    """Authenticate with Roboflow and download the dataset in YOLOv8 format."""
    try:
        from roboflow import Roboflow
    except ImportError:
        sys.exit("roboflow package not found. Run: pip install roboflow")

    print(f"\n[1/3] Authenticating with Roboflow …")
    rf = Roboflow(api_key=args.api_key)

    print(f"[2/3] Loading project  {args.workspace}/{args.project} v{args.version} …")
    project = rf.workspace(args.workspace).project(args.project)
    version = project.version(args.version)

    # YOLOv8 format gives us the train/valid/test split with YOLO-format labels
    # and a Roboflow-generated data.yaml.
    print(f"[3/3] Downloading dataset (YOLOv8 format) …")
    dataset = version.download("yolov8", location=args.location, overwrite=True)

    downloaded_path = Path(dataset.location)
    print(f"\n✓ Dataset saved to: {downloaded_path}")
    return downloaded_path


def patch_yaml(dataset_path: Path) -> None:
    """
    Overwrite configs/dataset.yaml with the absolute path to the downloaded
    data so every script can find it without any manual editing.
    """
    # Read the Roboflow-generated yaml to grab the class names (ground truth)
    roboflow_yaml = dataset_path / "data.yaml"
    class_names_str = ""
    if roboflow_yaml.exists():
        import yaml  # PyYAML is a transitive dep of ultralytics
        with open(roboflow_yaml) as f:
            rf_cfg = yaml.safe_load(f)
        names = rf_cfg.get("names", [
            "Hardhat", "Mask", "NO-Hardhat", "NO-Mask",
            "NO-Safety Vest", "Person", "Safety Cone", "Safety Vest"
        ])
        nc = len(names)
        # Build YAML-formatted names block
        class_names_str = "\n".join(f"  {i}: {n}" for i, n in enumerate(names))
    else:
        nc = 8
        class_names_str = (
            "  0: Hardhat\n  1: Mask\n  2: NO-Hardhat\n  3: NO-Mask\n"
            "  4: NO-Safety Vest\n  5: Person\n  6: Safety Cone\n  7: Safety Vest"
        )

    # Write a clean yaml that uses the absolute path — no ambiguity across OSes
    yaml_content = f"""\
# Auto-generated by data/download_dataset.py — do not edit manually.
# Re-run the download script to regenerate.

path: {dataset_path.as_posix()}  # absolute path to dataset root

train: train/images
val:   valid/images
test:  test/images

nc: {nc}

names:
{class_names_str}
"""
    DATASET_YAML.write_text(yaml_content)
    print(f"✓ configs/dataset.yaml updated → path = {dataset_path}")


def verify_splits(dataset_path: Path) -> None:
    """Quick sanity-check: confirm train/valid/test image folders exist."""
    for split in ("train", "valid", "test"):
        img_dir = dataset_path / split / "images"
        lbl_dir = dataset_path / split / "labels"
        n_imgs  = len(list(img_dir.glob("*"))) if img_dir.exists() else 0
        n_lbls  = len(list(lbl_dir.glob("*"))) if lbl_dir.exists() else 0
        status  = "✓" if n_imgs > 0 else "✗"
        print(f"  {status} {split:6s}  {n_imgs:5d} images  {n_lbls:5d} labels")


def main() -> None:
    args = parse_args()
    dataset_path = download(args)
    patch_yaml(dataset_path)
    print("\nDataset splits:")
    verify_splits(dataset_path)
    print(
        "\nAll done! Next step:\n"
        "  python src/preprocess.py --data configs/dataset.yaml\n"
    )


if __name__ == "__main__":
    main()
